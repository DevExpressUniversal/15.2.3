using System;
namespace DevExpress.Office.NumberConverters {
	#region RussianUpperNumberConverter
	public class RussianUpperNumberConverter : AlphabetBasedNumberConverter {
		static char[] russianUpper = { 'А', 'Б', 'В', 'Г', 'Д', 'Е', 'Ж', 'З', 'И', 'К', 'Л', 'М', 'Н', 'О', 'П', 'Р', 'С', 'Т', 'У', 'Ф', 'Х', 'Ц', 'Ч', 'Ш', 'Щ', 'Ы', 'Э', 'Ю', 'Я' };
		static int alphabetSize = russianUpper.Length;
		protected internal override char[] Alphabet { get { return russianUpper; } }
		protected internal override int AlphabetSize { get { return alphabetSize; } }
		protected internal override NumberingFormat Type { get { return NumberingFormat.RussianUpper; } }
	}
	#endregion
