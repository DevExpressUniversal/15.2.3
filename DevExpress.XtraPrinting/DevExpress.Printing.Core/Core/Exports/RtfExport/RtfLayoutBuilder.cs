using System;
using System.Drawing;
using DevExpress.XtraPrinting.Native;
using DevExpress.XtraPrinting.BrickExporters;
namespace DevExpress.XtraPrinting.Export {
	public class RtfLayoutBuilder : LayoutBuilder {
		RtfExportContext rtfExportContext;
		public RtfLayoutBuilder(Document document, RtfExportContext rtfExportContext)
			: base(document) {
			this.rtfExportContext = rtfExportContext;			
		}
		protected override ILayoutControl[] GetBrickLayoutControls(Brick brick, RectangleF rect) {
			BrickExporter exporter = BrickBaseExporter.GetExporter(rtfExportContext, brick) as BrickExporter;
			return ToLayoutControls(exporter.GetRtfData(rtfExportContext, rect, rect), brick);
		}
	}
}
