using System;
namespace DevExpress.XtraPrinting.Native {
	public static class ServiceProviderExtensions {
		public static T GetService<T>(this IServiceProvider serviceProvider) where T : class {
			return (serviceProvider == null) ? null : serviceProvider.GetService(typeof(T)) as T;
		}
		public static void PerformIfNotNull<T>(this IServiceProvider serviceProvider, Action<T> callback) where T : class {
			T serv = serviceProvider.GetService<T>();
			if(serv != null) callback(serv);
		}
		public static TCast GetService<TGet, TCast>(this IServiceProvider serviceProvider) where TCast : class {
			return (serviceProvider == null) ? null : serviceProvider.GetService(typeof(TGet)) as TCast;
		}
	}
}
