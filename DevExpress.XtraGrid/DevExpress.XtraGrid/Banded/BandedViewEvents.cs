	public class BandHeaderCustomDrawEventArgs : CustomDrawObjectEventArgs {
		public BandHeaderCustomDrawEventArgs(GraphicsCache cache, HeaderObjectPainter painter, GridBandInfoArgs info) : base(cache, painter, info, info.Appearance) {
		}
		public new GridBandInfoArgs Info { get { return base.Info as GridBandInfoArgs; } }
		public GridBand Band { get { return Info.Band; } }
	}
	public delegate void BandHeaderCustomDrawEventHandler(object sender, BandHeaderCustomDrawEventArgs e);
	public class BandEventArgs : EventArgs {
		GridBand band;
		public BandEventArgs(GridBand band) {
			this.band = band;
		}
		public GridBand Band { get { return band; } }
	}
	public delegate void BandEventHandler(object sender, BandEventArgs e);
