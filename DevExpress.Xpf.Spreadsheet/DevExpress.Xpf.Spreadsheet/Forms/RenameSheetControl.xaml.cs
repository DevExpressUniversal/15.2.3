namespace DevExpress.Xpf.Spreadsheet.Forms {
	public partial class RenameSheetControl : UserControl {
		public RenameSheetControl(RenameSheetViewModel viewModel) {
			InitializeComponent();
			DataContext = viewModel;
		}
		private void TextEditTextInput(object sender, TextCompositionEventArgs e) {
			TextEdit edit = sender as TextEdit;
			RenameSheetViewModel viewModel = DataContext as RenameSheetViewModel;
			char ch = e.Text[0];
			bool startWithSingleQuote = ch == '\'' && edit.SelectionStart == 0;
			if (startWithSingleQuote || viewModel.GetRestrictedCharacters().Contains(ch))
				e.Handled = true;
		}
	}
