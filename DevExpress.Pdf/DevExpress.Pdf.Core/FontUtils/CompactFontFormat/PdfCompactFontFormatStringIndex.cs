using System.Text;
using System.Collections.Generic;
namespace DevExpress.Pdf.Native {
	public class PdfCompactFontFormatStringIndex : PdfCompactFontFormatIndex {
		readonly string[] strings;
		public string[] Strings { get { return strings; } }
		public PdfCompactFontFormatStringIndex(PdfBinaryStream stream) : base(stream) {
			IList<byte[]> objects = Objects;
			int objectCount = objects.Count;
			strings = new string[objectCount];
			for (int i = 0; i < objectCount; i++) { 
				byte[] data = objects[i];
				strings[i] = Encoding.UTF8.GetString(data, 0, data.Length);
			}
		}	
	}
}
