using System;
using System.ComponentModel;
namespace DevExpress.XtraPdfViewer {
	public class PdfUriOpeningEventArgs : CancelEventArgs {
		readonly Uri uri;
		bool handled;
		public Uri Uri { get { return uri; } }
		public bool Handled {
			get { return handled; }
			set { handled = value; }
		}
		internal PdfUriOpeningEventArgs(Uri uri) : base(false) {
			this.uri = uri;
			this.handled = false;
		}
	}
	public delegate void PdfUriOpeningEventHandler(object sender, PdfUriOpeningEventArgs e);
}
