using System.Drawing;
namespace DevExpress.Pdf.Drawing {
	class PdfGraphicsDrawLineCommand : PdfGraphicsStrokingCommand {
		float x1;
		float x2;
		float y1;
		float y2;
		public PdfGraphicsDrawLineCommand(PdfPen pen, float x1, float y1, float x2, float y2)
			: base(pen) {
			this.x1 = x1;
			this.y1 = y1;
			this.x2 = x2;
			this.y2 = y2;
		}
		public override void Execute(PdfGraphicsPageContentsCommandConstructor constructor) {
			base.Execute(constructor);
			constructor.DrawLine(x1, y1, x2, y2);
		}
	}
}
