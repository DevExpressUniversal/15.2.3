using System.IO;
namespace DevExpress.Pdf.Drawing {
	public abstract class EmfShapeRecord : EmfRecord {
		readonly EmfRectL bounds;
		readonly EmfPointS[] aPoints;
		protected EmfPointS[] APoints { get { return aPoints; } }
		public EmfRectL Bounds { get { return bounds; } }
		protected EmfShapeRecord(byte[] content)
			: base(content) {
			BinaryReader contentStream = ContentStream;
			bounds = new EmfRectL(contentStream);
			int pointsCount = contentStream.ReadInt32();
			aPoints = new EmfPointS[pointsCount];
			for (int i = 0; i < pointsCount; i++) {
				aPoints[i] = new EmfPointS(contentStream);
			}
		}
	}
}
