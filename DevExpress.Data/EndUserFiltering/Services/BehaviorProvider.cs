namespace DevExpress.Utils.Filtering.Internal {
	using System.Collections;
	using System.Collections.Generic;
	public interface IBehaviorProvider {
		bool GetIsVisible(string name);
		bool GetIsEnabled(string name);
	}
	class BehaviorProvider : IBehaviorProvider {
		IDictionary<string, int> orders;
		Hashtable disabledElements;
		public BehaviorProvider(IDictionary<string, int> orders, Hashtable disabledElements) {
			this.orders = orders;
			this.disabledElements = disabledElements;
		}
		public bool GetIsEnabled(string name) {
			return !disabledElements.ContainsKey(name);
		}
		public bool GetIsVisible(string name) {
			int order;
			return !orders.TryGetValue(name, out order) || (order >= 0);
		}
	}
}
