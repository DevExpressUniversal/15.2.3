using System.Windows.Controls;
using System.Windows.Input;
namespace DevExpress.Design.Mvvm.Wizards.UI {
	public partial class TablesSelectorView : UserControl {
		public TablesSelectorView() {
			InitializeComponent();
		}
		void listBoxPreviewKeyDown(object sender, System.Windows.Input.KeyEventArgs e) {
			ListBox listBox = sender as ListBox;
			if(listBox == null)
				return;
			TablesSelectorViewModel viewModel = this.DataContext as TablesSelectorViewModel;
			if(viewModel == null || viewModel.SelectedInfo == null)
				return;
			if(e.Key == Key.Space) {
				viewModel.SelectedInfo.Checked = !viewModel.SelectedInfo.Checked;
				e.Handled = true;
			}
		}
	}
}
