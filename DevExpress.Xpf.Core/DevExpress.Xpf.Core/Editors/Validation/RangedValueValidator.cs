namespace DevExpress.Xpf.Editors.Validation.Native {
	public class RangedValueValidator<T> : TextEditValueValidator where T : struct, IComparable {
		RangeEditorStrategy<T> Strategy { get { return Editor.EditStrategy as RangeEditorStrategy<T>; } }
		public RangedValueValidator(TextEdit editor)
			: base(editor) {
		}
		public override object ProcessConversion(object value) {
			object converted = base.ProcessConversion(value);
			return Strategy.ShouldRoundToBounds ? Strategy.Correct(converted) : converted;
		}
		protected override bool IsValidValue(object value, object convertedValue) {
			return Strategy.InRange(convertedValue);
		}
		public override string GetValidationError() {
			return EditorLocalizer.GetString(EditorStringId.OutOfRange);
		}
	}
}
