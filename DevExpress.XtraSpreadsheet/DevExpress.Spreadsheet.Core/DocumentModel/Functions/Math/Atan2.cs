using System;
namespace DevExpress.XtraSpreadsheet.Model {
	#region FunctionAtan2
	public class FunctionAtan2 : WorksheetFunctionBase {
		static readonly FunctionParameterCollection functionParameters = PrepareParameters();
		public override string Name { get { return "ATAN2"; } }
		public override int Code { get { return 0x0061; } }
		public override FunctionParameterCollection Parameters { get { return functionParameters; } }
		public override OperandDataType ReturnDataType { get { return OperandDataType.Value; } }
		protected override VariantValue EvaluateCore(System.Collections.Generic.IList<VariantValue> arguments, WorkbookDataContext context) {
			VariantValue x = arguments[0].ToNumeric(context);
			if (x.IsError)
				return x;
			VariantValue y = arguments[1].ToNumeric(context);
			if (y.IsError)
				return y;
			return GetNumericResult(x.NumericValue, y.NumericValue);
		}
		VariantValue GetNumericResult(double x, double y) {
			if (x == 0 && y == 0)
				return VariantValue.ErrorDivisionByZero;
			return Math.Atan2(y, x);
		}
		static FunctionParameterCollection PrepareParameters() {
			FunctionParameterCollection collection = new FunctionParameterCollection();
			collection.Add(new FunctionParameter(OperandDataType.Value));
			collection.Add(new FunctionParameter(OperandDataType.Value));
			return collection;
		}
	}
	#endregion
}
