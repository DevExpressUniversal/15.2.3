using System.Windows.Media.Media3D;
using DevExpress.Charts.Native;
namespace DevExpress.Xpf.Charts.Native {
	public class AreaFullStackedSeries3DData : AreaStackedSeries3DData {
		public AreaFullStackedSeries3DData(AreaFullStackedSeries3D series) : base(series) {
		}
		protected override Point3D GetLabelPoint(XYDiagram3DDomain domain, RefinedPoint refinedPoint) {
			Point3D valuePoint = GetValuePoint(refinedPoint, domain);
			Point3D zeroPoint = GetZeroPoint(refinedPoint, domain);
			return new Point3D(valuePoint.X, (valuePoint.Y - zeroPoint.Y) / 2 + zeroPoint.Y, valuePoint.Z);
		}
	}
}
