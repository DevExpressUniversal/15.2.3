using System;
namespace DevExpress.Persistent.Base {
	public class UpdatableParameter : ParameterBase {
		private object currentValue;
		public UpdatableParameter(string name, Type valueType)
			: this(name, valueType, null) { }
		public UpdatableParameter(string name, Type valueType, object value)
			: base(name, valueType) {
			this.Visible = false;
			currentValue = value;
		}
		public void UpdateCurrentValue(object newValue) {
			SetCurrentValue(newValue);
		}
		protected override void SetCurrentValue(object value) {
			currentValue = value;
		}
		protected override object GetCurrentValue() { return currentValue; }
		public override bool IsReadOnly {
			get { return false; }
		}
	}
}
