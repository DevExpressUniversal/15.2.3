using System;
using System.Collections.Generic;
using System.Text;
using DevExpress.ExpressApp.Editors;
using DevExpress.ExpressApp.Model;
namespace DevExpress.ExpressApp.Web.Editors.ASPx {
	public class ASPxTimeSpanPropertyEditor : ASPxStringPropertyEditor {
		protected override object GetControlValueCore() {
			try {
				if(Editor.Text == EmptyValue)
					return new TimeSpan(0);
				else {
					try {
						return TimeSpan.FromMinutes(Int32.Parse(Editor.Text));
					}
					catch(FormatException) {
						return TimeSpan.Parse(Editor.Text);
					}
				}
			}
			catch {
				return new TimeSpan(0);
			}
		}
		public ASPxTimeSpanPropertyEditor(Type objectType, IModelMemberViewItem model) : base(objectType, model) { }
	}
}
