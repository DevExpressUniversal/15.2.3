using System;
using System.ComponentModel;
using DevExpress.ExpressApp.DC;
namespace DevExpress.ExpressApp.Win.Core.ModelEditor.InternalDesigner {
	[Browsable(false), EditorBrowsable(EditorBrowsableState.Never)]
	internal class DesignerPropertyDescriptor : XafPropertyDescriptor {
		public DesignerPropertyDescriptor(IMemberInfo memberInfo, String name)
			: base(memberInfo, name) {
		}
		public override object GetValue(object obj) {
			FakeObject fakeObject = obj as FakeObject;
			if(fakeObject != null) {
				return fakeObject.GetValue(MemberInfo);
			}
			else {
				return new FakeObject(100).GetValue(MemberInfo);
			}
		}
		public override void SetValue(object obj, object value) {
		}
	}
}
