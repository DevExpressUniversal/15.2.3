using System;
using DevExpress.DashboardCommon.Localization;
namespace DevExpress.DashboardCommon.Native {
	public static class KpiElementCaptionProvider {
		public static string GetCaption(Measure measure1, Measure measure2) {
			string measure1Caption = measure1 == null ? String.Empty : measure1.DisplayName,
				measure2Caption = measure2 == null ? String.Empty : measure2.DisplayName;
			if(String.IsNullOrEmpty(measure1Caption))
				return measure2Caption;
			return String.IsNullOrEmpty(measure2Caption) ? measure1Caption :
				string.Format(DashboardLocalizer.GetString(DashboardStringId.FormatStringKpiElementCaption), measure1Caption, measure2Caption);
		}
	}
}
