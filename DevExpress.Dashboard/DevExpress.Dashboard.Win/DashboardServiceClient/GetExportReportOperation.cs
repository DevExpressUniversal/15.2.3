using DevExpress.DashboardCommon.Service;
using DevExpress.DashboardExport;
using System.Collections;
namespace DevExpress.DashboardWin.Native {
	public class GetExportReportOperation : ClientOperationBase<ExportArgs, GetExportReportResult> {
		readonly ExportInfo exportInfo;
		public GetExportReportOperation(Hashtable clientState, ExportInfo exportInfo)
			: base(clientState) {
			this.exportInfo = exportInfo;
		}
		protected override ExportArgs CreateArgs() {
			return new ExportArgs() { ExportInfo = exportInfo, Exporter = DashboardExporter.Instance };
		}
		protected override GetExportReportResult ExecuteInternal(IDashboardService dashboardService, ExportArgs args) {
			return dashboardService.GetExportReport(args);
		}
	}
}
