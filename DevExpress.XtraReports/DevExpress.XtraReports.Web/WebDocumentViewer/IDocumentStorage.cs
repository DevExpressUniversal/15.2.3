using System;
namespace DevExpress.XtraReports.Web.WebDocumentViewer {
	public interface IDocumentStorage {
		string CreateNew();
		bool BlankExists(string id);
		void Update(string id, Action<IWebPreviewWritableDocument> action);
		T DoWithBuildResult<T>(string id, int? loadPageIndex, Func<WebPreviewReadableDocument, T> func);
		void Release(string id);
		void Clean(TimeSpan timeToLife);
	}
	public static class DocumentStorageExtensions {
		public static T DoWithBuildResult<T>(this IDocumentStorage documentStorage, string id, Func<WebPreviewReadableDocument, T> func) {
			return documentStorage.DoWithBuildResult(id, null, func);
		}
	}
}
