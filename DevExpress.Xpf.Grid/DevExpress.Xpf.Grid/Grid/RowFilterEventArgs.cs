using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Windows;
namespace DevExpress.Xpf.Grid {
	public class RowFilterEventArgs : EventArgs {
		int listSourceRowIndex;
		bool handled, visible;
		public RowFilterEventArgs(GridControl source, int listSourceRowIndex, bool fit) {
			Source = source;
			this.listSourceRowIndex = listSourceRowIndex;
			this.handled = false;
			this.visible = fit;
		}
		public bool Handled { get { return handled; } set { handled = value; } }
		public bool Visible { get { return visible; } set { visible = value; } }
		public int ListSourceRowIndex { get { return listSourceRowIndex; } }
		public GridControl Source { get; private set; }
	}
	public delegate void RowFilterEventHandler(object sender, RowFilterEventArgs e);
}
